String displayMedicineUnit(String unit) {
  switch (unit) {
    case 'CREME':
      return 'Crème';
    case 'POMMADE':
      return 'Pommade';
    case 'GELULE':
      return 'Gélule';
    case 'COMPRIME':
      return 'Comprimé';
    case 'GELE':
      return 'Gel';
    case 'SOLUTION_BUVABLE':
      return 'Solution buvable';
    case 'POUDRE':
      return 'Poudre';
    case 'SUPPOSITOIRE':
      return 'Suppositoire';
    case 'AMPOULE':
      return 'Ampoule';
    case 'SUSPENSION_NASALE':
      return 'Suspension nasale';
    case 'SPRAY':
      return 'Spray';
    case 'COLLUTOIRE':
      return 'Collutoire';
    case 'SHAMPOOING':
      return 'Shampooing';
    case 'SOLUTION_INJECTABLE':
      return 'Solution injectable';
    case 'COMPRIMER_EFERVESCENT':
      return 'Comprimé effervescent';
    case 'GRANULER_EN_SACHET':
      return 'Granulé en sachet';
    case 'PASTILLE':
      return 'Pastille';
    case 'SIROP':
      return 'Sirop';
    case 'SOLUTION_INHALATION':
      return 'Inhalation';
    case 'COMPRIME_ENROBE':
      return 'Comprimé';
    case 'CAPSULE_MOLLE':
      return 'Capsule molle';
    case 'COMPRIME_PELLICULE':
      return 'Comprimé pelliculé';
    default:
      return 'Unité';
  }
}
